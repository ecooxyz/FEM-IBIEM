      SUBROUTINE U0W0(ALF,IP,U0,W0)
	USE GROUP	 
      COMPLEX*16 A1,U0,W0,LXR,MXR,TR,SR
	DOUBLE PRECISION ALF,APR,ASVR
	  
	IAA=CMPLX(0.0D0,1.0D0)
	IF(IP==1)THEN
	  APR=1.0D0
	  ASVR=0.0D0   
	  LXR=DCOS(ALF)
	  MXR=CDSQRT(CSR/CPR)*LXR
      ELSE 
	  APR=0.0D0
	  ASVR=1.0D0   
	  MXR=DCOS(ALF)
	  LXR=CDSQRT(CPR/CSR)*MXR
      ENDIF
      TR=-IAA*SQRT(1.0D0-1.0D0/MXR**2)
	SR=-IAA*sqrt(1.0D0-1.0D0/LXR**2)
	A1=LXR*(1.0D0+TR*TR)/(4*SR*TR+(1.0D0-TR*TR)**2)
	U0=A1*(4.0D0*SR*TR*APR+SQRT(CSR/CPR)*2.0D0*TR*(1-TR*TR)*ASVR)
	W0=A1*(2.0D0*SR*(1-TR*TR)*APR-4.0D0*SR*TR*SQRT(CSR/CPR)*ASVR)
	RETURN
	END
